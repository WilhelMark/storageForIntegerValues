#include <iostream>
#include <vector>

int main() {
    std::vector<int> db(20); // Создаем вектор фиксированного размера 20
    int currentIndex = 0;     // Индекс для отслеживания текущей позиции в векторе
    int inputNumber;          // Переменная для хранения вводимого числа

    while (true) { // Бесконечный цикл для ввода данных
        std::cout << "input number: "; // Запрашиваем ввод числа
        std::cin >> inputNumber; // Считываем число с клавиатуры

        if (inputNumber == -1) { // Если введено -1, выходим из цикла
            break;
        }

        if (currentIndex < 20) { // Проверяем, не превышает ли индекс размер вектора
            db[currentIndex] = inputNumber; // Добавляем элемент в вектор
            currentIndex++; // Увеличиваем индекс
        } else {
            // Если вектор заполнен, перемещаем элементы
            for (int i = 1; i < 20; i++) {
                db[i - 1] = db[i]; // Сдвигаем элементы влево
            }
            db[19] = inputNumber; // Добавляем новый элемент в конец
        }
    }

    std::cout << "output: "; // Подготовка к выводу содержимого вектора
    for (int i = 0; i < currentIndex; i++) { // Проходим по всем введенным элементам
        std::cout << db[i] << " "; // Выводим элементы
    }
    std::cout << std::endl; // Переход на новую строку после вывода

    return 0; // Завершаем программу
}
